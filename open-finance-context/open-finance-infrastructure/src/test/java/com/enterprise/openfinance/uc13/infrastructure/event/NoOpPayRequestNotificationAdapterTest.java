package com.enterprise.openfinance.uc13.infrastructure.event;

import com.enterprise.openfinance.uc13.domain.model.PayRequest;
import com.enterprise.openfinance.uc13.domain.model.PayRequestStatus;
import org.junit.jupiter.api.Tag;
import org.junit.jupiter.api.Test;

import java.math.BigDecimal;
import java.time.Instant;

import static org.assertj.core.api.Assertions.assertThatCode;

@Tag("unit")
class NoOpPayRequestNotificationAdapterTest {

    @Test
    void shouldAllowNoOpNotifications() {
        NoOpPayRequestNotificationAdapter adapter = new NoOpPayRequestNotificationAdapter();
        PayRequest request = new PayRequest(
                "CONS-001",
                "TPP-001",
                "PSU-001",
                "Utilities Co",
                new BigDecimal("500.00"),
                "AED",
                PayRequestStatus.AWAITING_AUTHORISATION,
                Instant.parse("2026-02-10T10:00:00Z"),
                Instant.parse("2026-02-10T10:00:00Z"),
                null
        );

        assertThatCode(() -> adapter.notifyPayRequestCreated(request)).doesNotThrowAnyException();
        assertThatCode(() -> adapter.notifyPayRequestFinalized(request)).doesNotThrowAnyException();
    }
}
